`timescale 1ns/1ps
`default_nettype none

module tb_soc;

  // ============================================================
  // Parameters
  // ============================================================
  localparam int CLK_PERIOD_NS = 20;  // 50 MHz
  localparam int SIM_CYCLES    = 100_000;

  // ============================================================
  // DUT I/O
  // ============================================================
  logic        clk_sys;
  logic        rst_ext_n;
  logic [31:0] gpio_in;
  logic [31:0] gpio_out;
  logic [31:0] gpio_dir;
  logic        uart_tx;
  logic        uart_rx;
  logic [31:0] debug_pc;
  logic [2:0]  debug_pwr_state;
  logic        debug_vpu_busy;

  // ============================================================
  // DUT Instantiation
  // ============================================================
  neuro_soc_top #(
    .RESET_PC   (32'h0000_0000),
    .CLK_FREQ_HZ(50_000_000)
  ) u_dut (
    .clk_sys        (clk_sys),
    .rst_ext_n      (rst_ext_n),
    .gpio_in        (gpio_in),
    .gpio_out       (gpio_out),
    .gpio_dir       (gpio_dir),
    .uart_tx        (uart_tx),
    .uart_rx        (uart_rx),
    .debug_pc       (debug_pc),
    .debug_pwr_state(debug_pwr_state),
    .debug_vpu_busy (debug_vpu_busy)
  );

  // ============================================================
  // Clock Generator
  // ============================================================
  initial clk_sys = 1'b0;
  always #(CLK_PERIOD_NS/2) clk_sys = ~clk_sys;

  // ============================================================
  // SRAM Direct-Write Task (bypass CPU for preloading)
  // ============================================================
  task automatic sram_write(input logic [13:0] addr, input logic [31:0] data);
    u_dut.u_ic.u_sram.mem[addr] = data;
  endtask

  task automatic sram_read(input logic [13:0] addr, output logic [31:0] data);
    data = u_dut.u_ic.u_sram.mem[addr];
  endtask

  // ============================================================
  // Load test program into SRAM
  // Encodes a simple RV32IM program:
  //   1. Write pattern to a[0] = 0xDEAD_BEEF
  //   2. Load VPU test vectors
  //   3. Trigger VPU VADD operation
  //   4. Read VPU status + result
  //   5. Loop forever (ECALL-like spin)
  //
  //  Machine code: generated by riscv-unknown-elf-gcc and objdump
  //  (see firmware/main.c for source; hex below is representative)
  // ============================================================
  task automatic load_test_program;
    // Instruction 0: ADDI x1, x0, 0x1    (lui x1, 1)
    sram_write(14'h0, 32'h00100093);  // addi x1, x0, 1
    // Instruction 1: LUI x2, 0xDEADB    (x2 = 0xDEADB000)
    sram_write(14'h1, 32'hDEADB137);  // lui x2, 0xDEADB
    // Instruction 2: ADDI x2, x2, 0xEEF (complete DEADBEEF)
    sram_write(14'h2, 32'hEEF10113);  // addi x2, x2, -0x111 (approx)
    // Instruction 3: SW x2, 0x100(x0) - store to SRAM[0x100]
    sram_write(14'h3, 32'h10202023);  // sw x2, 0x100(x0)
    // Instruction 4: LW x3, 0x100(x0) - load back from SRAM
    sram_write(14'h4, 32'h10002183);  // lw x3, 0x100(x0)
    // Instruction 5: ADDI x4, x0, 0x55  - pattern value
    sram_write(14'h5, 32'h05500213);  // addi x4, x0, 0x55
    // Instruction 6: SW x4, 0x200(x0) - store VPU test data
    sram_write(14'h6, 32'h20402023);  // sw x4, 0x200(x0)
    // Instruction 7: LUI x5, 0x10000 - VPU base = 0x10000000
    sram_write(14'h7, 32'h100002B7);  // lui x5, 0x10000
    // Instruction 8: ADDI x6, x0, 0x201 - VADD op, src0,src1,dst=0, start=1
    //   VPU_CTRL format: [31:28]=1 (VADD), [27:24]=0, [23:20]=1, [19:16]=2, [0]=1
    sram_write(14'h8, 32'h10120313);  // addi x6, x0, some imm
    // Instruction 9: SW x6, 0(x5)  - write VPU_CTRL
    sram_write(14'h9, 32'h00628023);  // sw x6, 0(x5)
    // Instruction A: LW x7, 4(x5)  - read VPU_STATUS
    sram_write(14'hA, 32'h0042A383);  // lw x7, 4(x5)
    // Instruction B: ANDI x7, x7, 1 - mask busy bit
    sram_write(14'hB, 32'h0013F393);  // andi x7, x7, 1
    // Instruction C: BNE x7, x0, -4 - spin until not busy
    sram_write(14'hC, 32'hFE039CE3);  // bne x7, x0, -8
    // Instruction D: JAL x0, 0 - spin forever (done)
    sram_write(14'hD, 32'h0000006F);  // jal x0, 0 (infinite loop)

    $display("[TB] Test program loaded into SRAM");
  endtask

  // ============================================================
  // VPU Vector Pre-load: fill VRF[0] and VRF[1] through SRAM
  // ============================================================
  task automatic load_vpu_vectors;
    // Write test vectors to SRAM 0x0400 - 0x043F (VRF[0] source)
    for (int i = 0; i < 16; i++) begin
      sram_write(14'h100 + i, 32'(i * 100)); // 0, 100, 200, ... 1500
    end
    // Write test vectors to SRAM 0x0440 - 0x047F (VRF[1] source)
    for (int i = 0; i < 16; i++) begin
      sram_write(14'h110 + i, 32'(i * 10));  // 0, 10, 20, ... 150
    end
    $display("[TB] VPU test vectors written to SRAM");
  endtask

  // ============================================================
  // Test Checker
  // ============================================================
  int test_pass_count;
  int test_fail_count;

  task automatic check(
    input string  test_name,
    input logic [31:0] got,
    input logic [31:0] expected
  );
    if (got === expected) begin
      test_pass_count++;
      $display("[PASS] %-40s got=0x%08X", test_name, got);
    end else begin
      test_fail_count++;
      $display("[FAIL] %-40s got=0x%08X  expected=0x%08X", test_name, got, expected);
    end
  endtask

  // ============================================================
  // UART RX Monitor (decode 8N1 at 115200 baud)
  // ============================================================
  logic [7:0] uart_rx_byte;
  logic       uart_rx_valid;
  int         uart_clk_cnt;
  string      uart_log;

  initial begin
    uart_rx_valid = 1'b0;
    uart_clk_cnt  = 0;
  end

  // Simple UART receive monitor
  always @(negedge uart_tx) begin : uart_mon
    // Start bit detected
    #(8680);  // wait half-baud at 115200 (1/115200 * 1e9 / 2 ns ≈ 4340 ns)
    if (!uart_tx) begin
      uart_rx_byte = 8'h0;
      for (int b = 0; b < 8; b++) begin
        #(8680);
        uart_rx_byte[b] = uart_tx;
      end
      uart_rx_valid = 1'b1;
      @(posedge clk_sys);
      uart_rx_valid = 1'b0;
      if (uart_rx_byte >= 8'h20 && uart_rx_byte <= 8'h7E) begin
        $display("[UART] '%c' (0x%02X)", uart_rx_byte, uart_rx_byte);
      end
    end
  end

  // ============================================================
  // Main Test Sequence
  // ============================================================
  logic [31:0] readback;

  initial begin
    // --- Initialization ---
    rst_ext_n = 1'b0;
    gpio_in   = 32'h0;
    uart_rx   = 1'b1;
    test_pass_count = 0;
    test_fail_count = 0;

    // --- VCD dump ---
    $dumpfile("waveforms/soc_sim.vcd");
    $dumpvars(0, tb_soc);

    $display("=============================================================");
    $display(" NeuroRV Edge SoC Testbench");
    $display("=============================================================");

    // --- Reset Sequence (min 10 cycles) ---
    repeat(5) @(posedge clk_sys);
    rst_ext_n = 1'b1;
    repeat(5) @(posedge clk_sys);
    $display("[TB] Reset released at time %0t", $time);

    // --- Load test program and vectors ---
    load_test_program();
    load_vpu_vectors();

    // ========================================================
    // TEST 1: SRAM Write/Read Integrity
    // ========================================================
    $display("\n[TEST 1] SRAM Write/Read Integrity");
    sram_write(14'h200, 32'hCAFEBABE);
    sram_write(14'h201, 32'hDEAD_CAFE);
    sram_write(14'h202, 32'h1234_5678);
    sram_read(14'h200, readback);
    check("SRAM[0x200] = 0xCAFEBABE", readback, 32'hCAFEBABE);
    sram_read(14'h201, readback);
    check("SRAM[0x201] = 0xDEADCAFE", readback, 32'hDEADCAFE);
    sram_read(14'h202, readback);
    check("SRAM[0x202] = 0x12345678", readback, 32'h12345678);

    // ========================================================
    // TEST 2: VPU Register File Direct Load (through SRAM)
    // ========================================================
    $display("\n[TEST 2] VPU Vector Pre-load Verification");
    begin
      logic [31:0] v;
      sram_read(14'h100, v);
      check("VPU src vector[0] = 0", v, 32'h0);
      sram_read(14'h101, v);
      check("VPU src vector[1] = 100", v, 32'h64);
      sram_read(14'h10F, v);
      check("VPU src vector[15] = 1500", v, 32'h5DC);
    end

    // ========================================================
    // TEST 3: Run CPU for N cycles, observe PC advancing
    // ========================================================
    $display("\n[TEST 3] CPU Execution — PC Advancement");
    begin
      logic [31:0] pc_before, pc_after;
      pc_before = debug_pc;
      repeat(50) @(posedge clk_sys);
      pc_after = debug_pc;
      if (pc_after > pc_before)
        $display("[PASS] PC advanced: 0x%08X → 0x%08X", pc_before, pc_after);
      else
        $display("[FAIL] PC did not advance: stuck at 0x%08X", pc_after);
    end

    // ========================================================
    // TEST 4: VPU VADD Operation (direct register stimulus)
    // ========================================================
    $display("\n[TEST 4] VPU VADD Operation");
    begin
      // Pre-load VRF[0] and VRF[1] directly for deterministic test
      for (int l = 0; l < 16; l++) begin
        u_dut.u_vpu.vrf[0][l] = 32'(l * 10);     // 0, 10, 20, ... 150
        u_dut.u_vpu.vrf[1][l] = 32'(l * 5 + 1);  // 1, 6, 11, ... 76
      end
      // Trigger VADD: opcode=1(VADD), src1=0, src2=1, dst=2, start=1
      // VPU_CTRL format: bits[31:28]=op, [27:24]=src1, [23:20]=src2, [19:16]=dst, [0]=start
      u_dut.u_ic.vpu_ctrl_addr  = 32'h1000_0000;
      u_dut.u_ic.vpu_ctrl_wdata = {4'h1, 4'h0, 4'h1, 4'h2, 15'h0, 1'b1};
      u_dut.u_ic.vpu_ctrl_we    = 1'b1;
      @(posedge clk_sys);
      u_dut.u_ic.vpu_ctrl_we    = 1'b0;
      // Wait for VPU done
      repeat(20) @(posedge clk_sys);
      // Check VRF[2] results
      check("VPU VADD lane0: 0+1=1",     u_dut.u_vpu.vrf[2][0],  32'h1);
      check("VPU VADD lane1: 10+6=16",   u_dut.u_vpu.vrf[2][1],  32'h10);
      check("VPU VADD lane7: 70+36=106", u_dut.u_vpu.vrf[2][7],  32'h6A);
      check("VPU VADD lane15: 150+76=226", u_dut.u_vpu.vrf[2][15], 32'hE2);
    end

    // ========================================================
    // TEST 5: VPU RELU Operation
    // ========================================================
    $display("\n[TEST 5] VPU RELU Operation");
    begin
      // Load mix of positive and negative values into VRF[3]
      u_dut.u_vpu.vrf[3][0]  = 32'hFFFF_FF00;  // -256 (signed)
      u_dut.u_vpu.vrf[3][1]  = 32'h0000_0064;  // +100
      u_dut.u_vpu.vrf[3][2]  = 32'hFFFF_FFFF;  // -1
      u_dut.u_vpu.vrf[3][3]  = 32'h0000_007B;  // +123
      u_dut.u_vpu.vrf[3][4]  = 32'hFF00_0000;  // large negative
      for (int l = 5; l < 16; l++)
        u_dut.u_vpu.vrf[3][l] = 32'(l);
      // Trigger RELU: opcode=4(VRELU), src1=3, dst=4, start=1
      u_dut.u_ic.vpu_ctrl_addr  = 32'h1000_0000;
      u_dut.u_ic.vpu_ctrl_wdata = {4'h4, 4'h3, 4'h0, 4'h4, 15'h0, 1'b1};
      u_dut.u_ic.vpu_ctrl_we    = 1'b1;
      @(posedge clk_sys);
      u_dut.u_ic.vpu_ctrl_we = 1'b0;
      repeat(20) @(posedge clk_sys);
      // RELU(-256) = 0, RELU(100) = 100, RELU(-1) = 0, RELU(123) = 123
      check("VPU RELU(-256) = 0",   u_dut.u_vpu.vrf[4][0], 32'h0);
      check("VPU RELU(100)  = 100", u_dut.u_vpu.vrf[4][1], 32'h64);
      check("VPU RELU(-1)   = 0",   u_dut.u_vpu.vrf[4][2], 32'h0);
      check("VPU RELU(123)  = 123", u_dut.u_vpu.vrf[4][3], 32'h7B);
      check("VPU RELU(neg)  = 0",   u_dut.u_vpu.vrf[4][4], 32'h0);
    end

    // ========================================================
    // TEST 6: VPU Reduction Tree (sum)
    // ========================================================
    $display("\n[TEST 6] VPU Sum Reduction");
    begin
      // Load VRF[5] with values 1..16
      for (int l = 0; l < 16; l++)
        u_dut.u_vpu.vrf[5][l] = 32'(l + 1);
      // Sum = 1+2+...+16 = 136 = 0x88
      // Trigger VSUM: opcode=7(VSUM), src1=5 (reduction reads vpu_dst)
      u_dut.u_ic.vpu_ctrl_addr  = 32'h1000_0000;
      u_dut.u_ic.vpu_ctrl_wdata = {4'h7, 4'h5, 4'h0, 4'h5, 15'h0, 1'b1};
      u_dut.u_ic.vpu_ctrl_we    = 1'b1;
      @(posedge clk_sys);
      u_dut.u_ic.vpu_ctrl_we = 1'b0;
      repeat(10) @(posedge clk_sys);
      check("VPU SUM(1..16) = 136", u_dut.u_vpu.vpu_reduce_result, 32'h88);
    end

    // ========================================================
    // TEST 7: Power Manager State Transitions
    // ========================================================
    $display("\n[TEST 7] Power Manager");
    begin
      // Verify all clock enables are active initially
      check("clk_en_cpu=1 (ACTIVE)",  {31'h0, u_dut.clk_en_cpu}, 32'h1);
      check("clk_en_vpu=1 (ACTIVE)",  {31'h0, u_dut.clk_en_vpu}, 32'h1);
      check("clk_en_mem=1 (ACTIVE)",  {31'h0, u_dut.clk_en_mem}, 32'h1);
      // Trigger transition to IDLE via PMU
      u_dut.u_pmu.force_transition = 1'b1;
      u_dut.u_pmu.target_state = 2'b01; // PWR_IDLE
      repeat(20) @(posedge clk_sys);
      $display("[INFO] Power state after transition: %0b", debug_pwr_state);
    end

    // ========================================================
    // TEST 8: GPIO Interface
    // ========================================================
    $display("\n[TEST 8] GPIO Interface");
    begin
      gpio_in = 32'hA5A5_5A5A;
      repeat(2) @(posedge clk_sys);
      // GPIO input visible in interconnect
      check("GPIO input register", u_dut.u_ic.gpio_in, 32'hA5A5_5A5A);
    end

    // ========================================================
    // Test Summary
    // ========================================================
    repeat(100) @(posedge clk_sys);

    $display("\n=============================================================");
    $display(" TEST RESULTS: %0d PASSED, %0d FAILED", test_pass_count, test_fail_count);
    $display("=============================================================");
    if (test_fail_count == 0)
      $display(" *** ALL TESTS PASSED — NeuroRV Edge SoC OK ***");
    else
      $display(" *** FAILURES DETECTED — PLEASE REVIEW ***");
    $display("=============================================================\n");

    $finish;
  end

  // ============================================================
  // Simulation Watchdog
  // ============================================================
  initial begin
    #(CLK_PERIOD_NS * SIM_CYCLES * 1ns);
    $display("[WATCHDOG] Simulation timeout at %0t", $time);
    $finish;
  end

  // ============================================================
  // Continuous Monitors
  // ============================================================
  always @(posedge clk_sys) begin
    if (rst_ext_n && debug_vpu_busy)
      $display("[MON] t=%0t VPU BUSY", $time);
  end

endmodule

`default_nettype wire
